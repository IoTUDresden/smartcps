package eu.vicci.ecosystem.standalone.controlcenter.android.activities.general;

/**
 * The Interface for Gallery Activities.
 */
public interface GalleryActivity {

	/**
	 * Indicates, whether the gallery has been completely shown.
	 * 
	 * @return the gallery done
	 */
	public Boolean getGalleryDone();

	/**
	 * Sets the gallery done.
	 * 
	 * @param galleryDone
	 *            the new gallery done
	 */
	public void setGalleryDone(Boolean galleryDone);

}
